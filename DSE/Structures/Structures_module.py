#%% ------------ Functions -----------------
def monomial_basis(nodes, f):
	N = len(nodes);
	A = zeros((N, N));
	for i in range(N):
		A[:, i] = nodes**i;
	coeff = inv(A).dot(f);
	print(coeff);
	return coeff;

def reconstruct_f(coeff, plot_nodes):
	F = 0;
	for i in range(len(coeff)):
		F += coeff[i]*plot_nodes**i;
	return F;



